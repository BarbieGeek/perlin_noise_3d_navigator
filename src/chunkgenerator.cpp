//vgetNormal() finds the gradient of the scalar field at a point
//This gradient can be used as a very accurate vertx normal for lighting calculations
void  ChunkGenerator::getNormal( myGL::Vec3f &rfNormal, const float fX, const float fY, const float fZ )
{
    float   step_dec = _fStepSize * 0.01f;

    rfNormal.x = sample( fX - step_dec, fY, fZ ) - sample( fX + step_dec, fY, fZ );
    rfNormal.y = sample( fX, fY - step_dec, fZ ) - sample( fX, fY + step_dec, fZ );
    rfNormal.z = sample( fX, fY, fZ - step_dec ) - sample( fX, fY, fZ + step_dec );

    vNormalizeVector(rfNormal, rfNormal);
}




