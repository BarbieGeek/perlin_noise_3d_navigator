{
    int		iCorner, iVertex, iVertexTest, iEdge, iTriangle, iFlagIndex, iEdgeFlags;
    float     fOffset;
    myGL::Vec3f	sColor;
    float     afCubeValue[8];
    myGL::Vec3f	asEdgeVertex[12];
    myGL::Vec3f	asEdgeNorm[12];


    float fX = (float)D_POS.x + iX * fScale;
    float fY = (float)D_POS.y + iY * fScale;
    float fZ = (float)D_POS.z + iZ * fScale;


    //Make a local copy of the values at the cube's corners
    for (iVertex = 0; iVertex < 8; ++iVertex)
    {

#if D_USE_PERLIN_NOISE_CACHE == 1


#   define D_CACHE_INDEX_CUSTOM                                \
    D_CACHE_INDEX( iX + (int)a2fVertexOffset[iVertex][0],   \
    iY + (int)a2fVertexOffset[iVertex][1],   \
    iZ + (int)a2fVertexOffset[iVertex][2] )

        afCubeValue[iVertex] = _cache_perlinNoise[ D_CACHE_INDEX_CUSTOM ];

#   undef  D_CACHE_INDEX_CUSTOM


#else   // #if D_USE_PERLIN_NOISE_CACHE == 1

        afCubeValue[iVertex] = sample( fX + a2fVertexOffset[iVertex][0] * fScale,
                fY + a2fVertexOffset[iVertex][1] * fScale,
                fZ + a2fVertexOffset[iVertex][2] * fScale );

#endif  // #if D_USE_PERLIN_NOISE_CACHE == 1

    }

    //Find which vertices are inside of the surface and which are outside
    iFlagIndex = 0;
    for (iVertexTest = 0; iVertexTest < 8; ++iVertexTest)
    {
        if (afCubeValue[iVertexTest] <= fTv)
            iFlagIndex |= (1 << iVertexTest);
    }

    //Find which edges are intersected by the surface
    iEdgeFlags = aiCubeEdgeFlags[iFlagIndex];

    //If the cube is entirely inside or outside of the surface, then there will be no intersections
    if (iEdgeFlags == 0)
        return;

    //Find the point of intersection of the surface with each edge
    //Then find the normal to the surface at those points
    for (iEdge = 0; iEdge < 12; ++iEdge)
    {
        //if there is an intersection on this edge
        if (iEdgeFlags & (1 << iEdge))
        {
            fOffset = fgetOffset( afCubeValue[ a2iEdgeConnection[iEdge][0] ], afCubeValue[ a2iEdgeConnection[iEdge][1] ], fTv );

            asEdgeVertex[iEdge].x = fX + ( a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][0] + fOffset * a2fEdgeDirection[iEdge][0])  * fScale;
            asEdgeVertex[iEdge].y = fY + ( a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][1] + fOffset * a2fEdgeDirection[iEdge][1] ) * fScale;
            asEdgeVertex[iEdge].z = fZ + ( a2fVertexOffset[ a2iEdgeConnection[iEdge][0] ][2] + fOffset * a2fEdgeDirection[iEdge][2] ) * fScale;

            getNormal( asEdgeNorm[iEdge], asEdgeVertex[iEdge].x, asEdgeVertex[iEdge].y, asEdgeVertex[iEdge].z );
        }
    }


    //Draw the triangles that were found.  There can be up to five per cube
    for (iTriangle = 0; iTriangle < 5; ++iTriangle)
    {
        if (a2iTriangleConnectionTable[ iFlagIndex ][ 3 * iTriangle ] < 0)
            break;


        int     next_index = D_INDICES.size();

        for (iCorner = 0; iCorner < 3; ++iCorner)
        {
            iVertex = a2iTriangleConnectionTable[ iFlagIndex ][ 3 * iTriangle + iCorner ];

            vgetColor( sColor, asEdgeNorm[iVertex] );

            ///

            /**
            glColor4f( sColor.x, sColor.y, sColor.z, 0.6 );
            glNormal3f( asEdgeNorm[iVertex].x, asEdgeNorm[iVertex].y, asEdgeNorm[iVertex].z );
            glVertex3f( asEdgeVertex[iVertex].x, asEdgeVertex[iVertex].y, asEdgeVertex[iVertex].z );
            //*/

            ////

            myGL::Vec3f vertex = asEdgeVertex[iVertex];

            vertex *= (float)_chunkSize;

            //

            /// THIS IS SLOW
            // can be speed up by using a cache that store the neighbors triangles indices
            // there is a maximum of 5 triangles by "cube"
            // to find a triangle, stock only the first of the 3 indices can be enought since the others are just the two next ones
            // the index of the indices_tab can be enought for size_optimization

            //

            /**/

#if D_USE_TRIANGLES_INDICES_CACHE == 1

            {

                bool    found_one = false;

                /**/

                int min_x = std::max( 0, iX - 1 );
                int min_y = std::max( 0, iY - 1 );
                int min_z = std::max( 0, iZ - 1 );

                int max_x = std::min( (int)_chunkSize - 1, iX + 1 );
                int max_y = std::min( (int)_chunkSize - 1, iY + 1 );
                int max_z = std::min( (int)_chunkSize - 1, iZ + 1 );

                for (int z = min_z; !found_one && z <= max_z; ++z)
                    for (int y = min_y; !found_one && y <= max_y; ++y)
                        for (int x = min_x; !found_one && x <= max_x; ++x)
                            for ( int i_cache = 0; !found_one && i_cache < 5; ++i_cache )
                            {
                                int curr_idx = D_TRICACHE(x, y, z, i_cache);

                                if (curr_idx == -1 || curr_idx >= (int)D_INDICES.size())
                                    break;

                                for (int i_vertex = 0; !found_one && i_vertex < 3; ++i_vertex)
                                    if (vertex == D_VERTICES[ D_INDICES[ curr_idx + i_vertex ] ]._coord)
                                    {
                                        D_INDICES.push_back( D_INDICES[ curr_idx + i_vertex ] );
                                        found_one = true;
                                    }

                            }

                //*/

                if (!found_one)
                {
                    D_INDICES.push_back( D_VERTICES.size() );

                    D_VERTICES.push_back( t_Vertex( vertex, sColor ) );
                    D_VERTICES.back()._normal = asEdgeNorm[iVertex];
                }

            }

#else   // #if D_USE_TRIANGLES_INDICES_CACHE == 1

            {
                unsigned int    idx = 0;

                for ( ; idx < _Vertices.size(); ++idx )
                    if (vertex == _Vertices[idx]._coord)
                    {
                        _Indices.push_back(idx);
                        break;
                    }
                /// /THIS IS SLOW

                if (idx == _Vertices.size())
                {
                    _Indices.push_back( idx );
                    _Vertices.push_back( t_Vertex( vertex, sColor ) );
                    _Vertices.back()._normal = asEdgeNorm[iVertex];
                }
            }

#endif  // #if D_USE_TRIANGLES_INDICES_CACHE == 1

        }

        ///

#if D_USE_TRIANGLES_INDICES_CACHE == 1

        for ( int i_cache = 0; i_cache < 5; ++i_cache )
            if (D_TRICACHE(iX, iY, iZ, i_cache) == -1)
            {
                D_TRICACHE(iX, iY, iZ, i_cache) = next_index;
                break;
            }

#endif  // #if D_USE_TRIANGLES_INDICES_CACHE == 1

        ///

    }

}


