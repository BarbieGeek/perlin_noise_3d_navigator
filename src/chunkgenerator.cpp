void    ChunkGenerator::run()
{
    while (!_abort)
    {
        _condition.wait( &_mutex );

        if (_pPerlin3D_Chunk)
        {
            marchingCubes();

            _pPerlin3D_Chunk->_is_computed = false;
            _pPerlin3D_Chunk->_enabled = true;
        }

        _inUse = false;
    }
}


bool    ChunkGenerator::setChunkSize(unsigned int chunkSize)
{
    if (_inUse)
        return (false);

    _chunkSize = chunkSize;
    _fStepSize = 1.0f / _chunkSize;

    delete[] _cache_perlinNoise, _cache_perlinNoise = NULL;
    delete[] _cache_trianglesIndices, _cache_trianglesIndices = NULL;

    return (true);
}



ChunkGenerator::ChunkGenerator(QObject* parent)
    : QThread(parent),
      _inUse(false),
      _abort(false),

      _chunkSize(20),
      _fStepSize(1.0f / _chunkSize),

      _cache_perlinNoise(NULL),
      _cache_trianglesIndices(NULL),

      _pPerlin3D_Chunk(NULL)
{
    {
        int     octaves = 1;
        float   freq = 1.0f;
        float   amp = 1.0f;
        int     seed = 0;

        _PerlinNoise.Set( octaves, freq, amp, seed );
    }

    //this->start( LowPriority );
    this->start( QThread::HighestPriority );
}

ChunkGenerator::~ChunkGenerator()
{
    _mutex.lock();
    {
        _abort = true;
        //_pChunk = NULL;
        _condition.wakeOne();
    }
    _mutex.unlock();

    this->wait();
    _inUse = false;

    delete[] _cache_perlinNoise;
    delete[] _cache_trianglesIndices;
}



bool    ChunkGenerator::generate( const myGL::Vec3i& pos,
                  Perlin3D_Chunk* pPerlin3D_Chunk,
                  bool use_thread /*= true*/ )
{
    if (_inUse)
        return (false);
    _inUse = true;

#define D_CUBE(l_value) (l_value) * (l_value) * (l_value)

    if (!_cache_perlinNoise)        _cache_perlinNoise = new float[ D_CUBE(_chunkSize + 1) ];
    if (!_cache_trianglesIndices)   _cache_trianglesIndices = new int[ D_CUBE(_chunkSize) * 5 ];

#undef  D_CUBE

    _pPerlin3D_Chunk = pPerlin3D_Chunk;
    _pPerlin3D_Chunk->_pos = pos;
    _pPerlin3D_Chunk->_sideSize = (float)_chunkSize;

    _pPerlin3D_Chunk->_enabled = false;

    ///

    if (use_thread)
    {
        QMutexLocker    locker( &_mutex );

        _condition.wakeOne();
    }
    else
    {
        marchingCubes();

        _pPerlin3D_Chunk->_is_computed = false;
        _pPerlin3D_Chunk->_enabled = true;
    }

    return (true);
}



